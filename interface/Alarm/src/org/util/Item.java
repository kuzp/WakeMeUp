package org.util;

public class Item{
	public String Id;
	public String time;
	public String address;
	public String discipline;
	public String type;
	public String tutor;
	Item(String Id, String time, String address, String discipline,	String type,String tutor){
		this.Id = Id;
		this.time = time;
		this.address = address;
		this.discipline = discipline;
		this.type = type;
		this.tutor = tutor;
	}
}